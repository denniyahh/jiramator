"""Setup wizard — interactive first-run onboarding for non-technical users.

This is the brains of the ``init`` command. Its job is to remove the hardest
part of getting started with Jiramator: hand-editing YAML full of raw Jira
custom-field IDs. It:

    1. Collects the Jira URL and credentials, and tests the connection live.
    2. Auto-discovers custom field IDs *by name* via the Jira API, so the user
       picks "Story Points" instead of ``customfield_10026``.
    3. Validates the project key against Jira.
    4. Writes a ready-to-use org config and a commented team-config skeleton
       full of common, customizable examples.
    5. Sets up credentials (printed export commands or a gitignored ``.env``).

Interactive I/O lives here at the edge; the payload-building helpers
(``build_org_config_yaml``, ``build_team_skeleton_yaml``, ``discover_field_id``,
etc.) are pure and unit-tested. All real Jira communication goes through the
existing :class:`~jiramator.jira_client.JiraClient`.
"""

from __future__ import annotations

import os
import re
from pathlib import Path

from rich.console import Console
from rich.prompt import Confirm, IntPrompt, Prompt
from rich.table import Table

from jiramator.config import OrgConfig, SprintConfig
from jiramator.jira_client import JiraApiError, JiraClient

# ---------------------------------------------------------------------------
# Core field discovery
# ---------------------------------------------------------------------------

# Logical field name -> ordered list of likely Jira field display names.
# The first case-insensitive match wins. These are the fields `plan` relies on;
# users can map additional fields later for import/update (see the wiki).
_CORE_FIELD_CANDIDATES: dict[str, list[str]] = {
    "epic_link": ["Epic Link", "Parent Link", "Parent"],
    "story_points": ["Story Points", "Story point estimate", "Story Point Estimate"],
    "sprint_field": ["Sprint"],
}


def discover_field_id(
    fields: list[dict[str, object]], candidates: list[str]
) -> str | None:
    """Return the Jira field id whose name matches one of *candidates*.

    Matching is case-insensitive and honors candidate priority order (an
    earlier candidate always wins over a later one).

    Args:
        fields: Field metadata as returned by ``JiraClient.get_fields()``
            (each item has ``"id"`` and ``"name"`` keys).
        candidates: Ordered display names to look for.

    Returns:
        The matching field id, or ``None`` if nothing matched.
    """
    by_name: dict[str, str] = {}
    for f in fields:
        name = str(f.get("name", "")).strip().lower()
        fid = str(f.get("id", "")).strip()
        # Keep the first id seen for a given name (Jira may list duplicates).
        if name and fid and name not in by_name:
            by_name[name] = fid
    for candidate in candidates:
        fid = by_name.get(candidate.strip().lower())
        if fid is not None:
            return fid
    return None


# ---------------------------------------------------------------------------
# Pure helpers — slugs and YAML rendering
# ---------------------------------------------------------------------------


def slugify(name: str) -> str:
    """Turn a free-text name into a safe lowercase filename slug.

    ``"My Team!"`` -> ``"my-team"``. Falls back to ``"config"`` if empty.
    """
    slug = re.sub(r"[^a-z0-9]+", "-", name.strip().lower()).strip("-")
    return slug or "config"


def build_org_config_yaml(
    *,
    jira_url: str,
    jira_email_env: str,
    jira_token_env: str,
    custom_fields: dict[str, str],
    sprints: SprintConfig,
) -> str:
    """Render a ready-to-use org config YAML string.

    Only the fields ``plan`` needs are emitted; ``bulk_create`` (used by
    ``import``/``update``) is intentionally omitted and falls back to its
    defaults — a comment points users to the wiki for that.
    """
    lines: list[str] = [
        "# Org-level Jiramator configuration — generated by `jiramator init`.",
        "# Safe to edit by hand. Never put credentials in this file; they are",
        "# read from environment variables at runtime.",
        "",
        f"jira_url: {jira_url}",
        "",
        "# Credentials are read from these environment variables at runtime.",
        f"jira_email_env: {jira_email_env}",
        f"jira_token_env: {jira_token_env}",
        "",
        "# Logical name -> Jira custom field id, auto-discovered from your Jira.",
        "custom_fields:",
    ]
    if custom_fields:
        width = max(len(k) for k in custom_fields) + 1
        for name, fid in custom_fields.items():
            lines.append(f"  {(name + ':').ljust(width)} {fid}")
    else:
        lines.append("  {}")
    lines += [
        "",
        "# Bulk import/update (the `import` and `update` commands) needs an",
        "# additional `bulk_create:` block (field aliases + types). See:",
        "# https://github.com/dkim_mktx/jiramator/wiki/Import-and-Update-Details",
        "",
        "# PI sprint cadence.",
        "sprints:",
        f"  count: {sprints.count}",
        f"  standard_length_weeks: {sprints.standard_length_weeks}",
        f"  long_length_weeks: {sprints.long_length_weeks}",
        f"  long_sprints: [{', '.join(str(n) for n in sprints.long_sprints)}]",
        "",
    ]
    return "\n".join(lines)


def build_team_skeleton_yaml(
    *,
    project_key: str,
    team_name: str,
    epic_link_field: str,
) -> str:
    """Render a commented team-config skeleton with customizable examples.

    The examples are deliberately concrete (a regression-test per-release
    ticket and a prod-support per-sprint ticket) so a user can adapt them
    rather than build templates from scratch. ``{variables}`` are interpolated
    by the ticket builder at run time.
    """
    epic = epic_link_field or "customfield_XXXXX"
    return f"""\
# Team-level Jiramator configuration — generated by `jiramator init`.
#
# This is a STARTING POINT: the epics and ticket templates below are common
# examples. Edit them to match the tickets YOUR team creates every PI, then
# preview with:  jiramator plan --dry-run
#
# Template variables you can use anywhere in summaries/fields:
#   {{pi_num}}     -> e.g. 29          {{pi_label}}   -> e.g. PI29
#   {{version}}    -> a fix version    {{sprint_num}} -> sprint number
#   {{team_name}}  -> {team_name}

project_key: {project_key}
team_name: {team_name}

# Board id enables sprint assignment. Find it in your Jira board URL
# (…/RapidBoard.jspa?rapidView=<board_id>). Remove this line if unused.
# board_id: 123

# Whether this PI's sprints already exist in Jira. Set false to skip the
# board lookup, or omit to be asked each run.
sprints_exist: false

# ---------------------------------------------------------------------------
# Reuse epics that already exist in Jira instead of creating new ones.
# Map a short reference key to its real Jira issue key, then point tickets at
# it with "$epic:<key>" (see the examples below).
# ---------------------------------------------------------------------------
existing_epics:
  misc: {project_key}-1        # <-- replace with a real epic key, or delete

# ---------------------------------------------------------------------------
# Epics created fresh at the start of each PI. Leave empty if you only reuse
# existing epics above.
# ---------------------------------------------------------------------------
recurring_epics: []
# Example:
# recurring_epics:
#   - key: bau
#     summary: "{{team_name}} {{pi_label}} - Business as Usual"
#     fields:
#       issuetype: Epic

# ---------------------------------------------------------------------------
# Tickets generated once PER RELEASE version.
# With 2 fix versions, one template here produces 2 tickets.
# ---------------------------------------------------------------------------
per_release_tickets:
  - summary: "Testing - {{version}} Regression test"
    fields:
      issuetype: Task
      priority: Medium
      labels: ["{{pi_label}}", "Testing"]
      fixVersions: ["{{version}}"]
      {epic}: "$epic:misc"     # link to the 'misc' epic above

# ---------------------------------------------------------------------------
# Tickets generated once PER SPRINT.
# Standard sprints: 1 ticket each. Long sprints: extra tickets with a suffix.
# ---------------------------------------------------------------------------
per_sprint_tickets:
  - summary: "Prod Support (Sprint {{sprint_num}})"
    fields:
      issuetype: Task
      priority: Medium
      labels: ["{{pi_label}}", "Prod_Support"]
      {epic}: "$epic:misc"
    extra_on_long_sprint: 1
    long_sprint_suffix: ["a", "b"]   # e.g. Sprint 6 -> "6a" + "6b"
"""


def render_env_exports(email_env: str, token_env: str, email: str, token: str) -> str:
    """Render copy-paste shell export commands (bash/zsh and PowerShell)."""
    return (
        "# macOS / Linux (bash/zsh):\n"
        f"export {email_env}='{email}'\n"
        f"export {token_env}='{token}'\n"
        "\n"
        "# Windows PowerShell:\n"
        f"$env:{email_env}='{email}'\n"
        f"$env:{token_env}='{token}'"
    )


def render_env_file(email_env: str, token_env: str, email: str, token: str) -> str:
    """Render the contents of a ``.env`` file holding the credentials."""
    return (
        "# Jiramator credentials — generated by `jiramator init`.\n"
        "# This file contains a secret; it is gitignored. Do not commit or share it.\n"
        f"{email_env}={email}\n"
        f"{token_env}={token}\n"
    )


# ---------------------------------------------------------------------------
# .env / .gitignore helpers
# ---------------------------------------------------------------------------


def _ensure_gitignored(entry: str, gitignore: Path) -> None:
    """Append *entry* to *gitignore* if not already present."""
    existing = ""
    if gitignore.exists():
        existing = gitignore.read_text(encoding="utf-8")
        if any(line.strip() == entry for line in existing.splitlines()):
            return
    prefix = "" if existing.endswith("\n") or not existing else "\n"
    with gitignore.open("a", encoding="utf-8") as fh:
        fh.write(f"{prefix}{entry}\n")


