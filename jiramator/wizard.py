def _prompt_jira_url(console: Console) -> str:
    """Prompt for the Jira base URL and validate it looks like a URL."""
    while True:
        url = Prompt.ask(
            "[bold]Your Jira base URL[/] (e.g. https://acme.atlassian.net)",
            console=console,
        ).strip().rstrip("/")
        if url.startswith(("http://", "https://")) and "." in url:
            return url
        console.print("[red]Please enter a full URL starting with https://[/]")


def _connect(
    console: Console, jira_url: str, email_env: str, token_env: str
) -> tuple[JiraClient, str, str]:
    """Prompt for credentials and return a live, verified client.

    Loops until the credentials authenticate. Sets the credentials in the
    process environment so the constructed client (and the rest of the wizard)
    can use them.

    Returns:
        (client, email, token)
    """
    while True:
        email = Prompt.ask("[bold]Your Jira email[/]", console=console).strip()
        token = Prompt.ask(
            "[bold]Your Jira API token[/] "
            "(create one at id.atlassian.com/manage-profile/security/api-tokens)",
            password=True,
            console=console,
        ).strip()

        os.environ[email_env] = email
        os.environ[token_env] = token

        placeholder_sprints = SprintConfig(
            count=6, standard_length_weeks=2, long_length_weeks=3, long_sprints=[6]
        )
        try:
            org = OrgConfig(
                jira_url=jira_url,  # type: ignore[arg-type]
                jira_email_env=email_env,
                jira_token_env=token_env,
                custom_fields={},
                sprints=placeholder_sprints,
            )
            client = JiraClient(org)
            console.print("  [dim]Testing connection…[/]")
            client.get_fields()  # cheap authenticated call
        except JiraApiError as exc:
            console.print(f"[red]Connection failed:[/] {exc}")
            if not Confirm.ask("Try different credentials?", default=True, console=console):
                raise
            continue
        except ValueError as exc:
            console.print(f"[red]{exc}[/]")
            continue

        console.print("[green]✓[/] Connected to Jira.")
        return client, email, token


def _discover_core_fields(
    console: Console, client: JiraClient
) -> dict[str, str]:
    """Auto-discover the core custom field ids, confirming each with the user."""
    fields = client.get_fields()
    resolved: dict[str, str] = {}

    console.print("\n[bold]── Discovering your Jira custom fields ──[/]\n")
    for logical, candidates in _CORE_FIELD_CANDIDATES.items():
        found = discover_field_id(fields, candidates)
        if found is not None:
            console.print(
                f"  [green]✓[/] {logical}: matched [cyan]{found}[/] "
                f"([dim]{candidates[0]}[/])"
            )
            resolved[logical] = found
        else:
            console.print(
                f"  [yellow]?[/] Could not auto-detect [bold]{logical}[/] "
                f"(looked for: {', '.join(candidates)})."
            )
            manual = Prompt.ask(
                f"    Enter the field id for {logical} "
                f"(e.g. customfield_10014), or leave blank to skip",
                default="",
                console=console,
            ).strip()
            if manual:
                resolved[logical] = manual
    return resolved


def _prompt_sprint_cadence(console: Console) -> SprintConfig:
    """Prompt for the PI sprint cadence."""
    console.print("\n[bold]── Sprint cadence ──[/]\n")
    count = IntPrompt.ask("How many sprints in a PI?", default=6, console=console)
    standard = IntPrompt.ask(
        "Standard sprint length (weeks)?", default=2, console=console
    )
    long_len = IntPrompt.ask(
        "Long sprint length (weeks)?", default=3, console=console
    )
    long_raw = Prompt.ask(
        "Which sprint numbers are long? (comma-separated, blank for none)",
        default=str(count),
        console=console,
    ).strip()
    long_sprints = [
        int(n) for n in re.split(r"[,\s]+", long_raw) if n.strip().isdigit()
    ]
    return SprintConfig(
        count=count,
        standard_length_weeks=standard,
        long_length_weeks=long_len,
        long_sprints=long_sprints,
    )


def _setup_credentials(
    console: Console, email_env: str, token_env: str, email: str, token: str, cwd: Path
) -> None:
    """Let the user pick how credentials are provided going forward."""
    console.print("\n[bold]── Credentials ──[/]\n")
    console.print(
        "Jiramator reads your token from an environment variable — it is never "
        "stored in config. How would you like to provide it going forward?"
    )
    choice = Prompt.ask(
        "  [1] Show me export commands to paste  "
        "[2] Save a gitignored .env file for me",
        choices=["1", "2"],
        default="1",
        console=console,
    )
    if choice == "2":
        env_path = cwd / ".env"
        env_path.write_text(
            render_env_file(email_env, token_env, email, token), encoding="utf-8"
        )
        _ensure_gitignored(".env", cwd / ".gitignore")
        console.print(
            f"[green]✓[/] Wrote [bold]{env_path}[/] (gitignored). Load it each "
            "session with:  [cyan]set -a && source .env && set +a[/]"
        )
    else:
        console.print(
            "\nPaste these into your terminal (they last for the current "
            "session):\n"
        )
        console.print(render_env_exports(email_env, token_env, email, token))


def run_init(
    console: Console | None = None,
    *,
    org_dir: Path | None = None,
    team_dir: Path | None = None,
    cwd: Path | None = None,
    force: bool = False,
) -> None:
    """Run the interactive setup wizard.

    Args:
        console: Rich console for output (created if omitted).
        org_dir: Directory to write the org config into (default ``configs/org``).
        team_dir: Directory for the team config (default ``configs/teams``).
        cwd: Base directory for ``.env``/``.gitignore`` (default: current dir).
        force: Overwrite existing config files without asking.
    """
    if console is None:
        console = Console(stderr=True)
    org_dir = org_dir or Path("configs/org")
    team_dir = team_dir or Path("configs/teams")
    cwd = cwd or Path.cwd()

    console.print("\n[bold]══ Jiramator setup ══[/]\n")
    console.print(
        "This will connect to Jira, discover your field ids, and write your "
        "org and team config files. Let's go.\n"
    )

    email_env, token_env = "JIRA_EMAIL", "JIRA_TOKEN"

    jira_url = _prompt_jira_url(console)
    client, email, token = _connect(console, jira_url, email_env, token_env)

    custom_fields = _discover_core_fields(console, client)

    # -- Project key (validated) -------------------------------------------
    console.print("\n[bold]── Your team ──[/]\n")
    while True:
        project_key = Prompt.ask(
            "[bold]Jira project key[/] (e.g. CA)", console=console
        ).strip().upper()
        try:
            project = client.get_project(project_key)
            console.print(
                f"[green]✓[/] Found project [bold]{project_key}[/] "
                f"({project.get('name', project_key)})."
            )
            break
        except JiraApiError as exc:
            console.print(f"[red]{exc}[/]")
            if not Confirm.ask("Try a different key?", default=True, console=console):
                break

    team_name = Prompt.ask(
        "[bold]Team name[/] (used in ticket summaries)", console=console
    ).strip()

    sprints = _prompt_sprint_cadence(console)

    # -- Write config files -------------------------------------------------
    org_dir.mkdir(parents=True, exist_ok=True)
    team_dir.mkdir(parents=True, exist_ok=True)
    org_path = org_dir / f"{slugify(team_name)}.yaml"
    team_path = team_dir / f"{slugify(team_name)}.yaml"

    for path in (org_path, team_path):
        if path.exists() and not force:
            if not Confirm.ask(
                f"[yellow]{path} exists. Overwrite?[/]", default=False, console=console
            ):
                console.print("[red]Aborted.[/] Existing config left untouched.")
                return

    org_path.write_text(
        build_org_config_yaml(
            jira_url=jira_url,
            jira_email_env=email_env,
            jira_token_env=token_env,
            custom_fields=custom_fields,
            sprints=sprints,
        ),
        encoding="utf-8",
    )
    team_path.write_text(
        build_team_skeleton_yaml(
            project_key=project_key,
            team_name=team_name,
            epic_link_field=custom_fields.get("epic_link", ""),
        ),
        encoding="utf-8",
    )
    console.print(f"\n[green]✓[/] Wrote org config:  [bold]{org_path}[/]")
    console.print(f"[green]✓[/] Wrote team config: [bold]{team_path}[/]")

    _setup_credentials(console, email_env, token_env, email, token, cwd)

    # -- Next steps ---------------------------------------------------------
    console.print("\n[bold green]══ Setup complete ══[/]\n")
    table = Table(show_header=False, box=None, pad_edge=False)
    table.add_column(style="dim")
    table.add_row("1.", f"Edit your team's tickets in [cyan]{team_path}[/]")
    table.add_row("2.", "Preview a PI (creates nothing): [cyan]jiramator plan --dry-run[/]")
    table.add_row("3.", "When it looks right, run [cyan]jiramator plan[/] to create them.")
    console.print(table)
    console.print(
        "\n[dim]Full docs: https://github.com/dkim_mktx/jiramator/wiki[/]\n"
    )
